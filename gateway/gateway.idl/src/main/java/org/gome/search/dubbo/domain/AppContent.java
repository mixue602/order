package org.gome.search.dubbo.domain;

import com.alibaba.fastjson.JSONArray;
import com.alibaba.fastjson.JSONObject;

import java.io.Serializable;

public class AppContent implements Serializable {
        private JSONObject category = new JSONObject();
        private JSONArray prodInfo = new JSONArray();
        private JSONObject facets = new JSONObject();
        private JSONObject pageBar = new JSONObject();
        private JSONArray promoInfo = new JSONArray();
        private JSONObject selectData = new JSONObject();
        private JSONObject banner = new JSONObject();
        private JSONArray activities = new JSONArray();
        private JSONObject commonInfo = new JSONObject();

        public JSONObject getCategory() {
            return this.category;
        }

        public void setCategory(JSONObject category) {
            this.category = category;
        }

        public JSONArray getProdInfo() {
            return this.prodInfo;
        }

        public void setProdInfo(JSONArray prodInfo) {
            this.prodInfo = prodInfo;
        }

        public JSONObject getFacets() {
            return this.facets;
        }

        public void setFacets(JSONObject facets) {
            this.facets = facets;
        }

        public JSONObject getPageBar() {
            return this.pageBar;
        }

        public void setPageBar(JSONObject pageBar) {
            this.pageBar = pageBar;
        }

        public JSONArray getPromoInfo() {
            return this.promoInfo;
        }

        public void setPromoInfo(JSONArray promoInfo) {
            this.promoInfo = promoInfo;
        }

        public JSONObject getSelectData() {
            return this.selectData;
        }

        public void setSelectData(JSONObject selectData) {
            this.selectData = selectData;
        }

        public JSONObject getBanner() {
            return this.banner;
        }

        public void setBanner(JSONObject banner) {
            this.banner = banner;
        }
        public JSONArray getActivities() {
            return this.activities;
        }

        public void setActivities(JSONArray activities) {
            this.activities = activities;
        }

        public JSONObject getCommonInfo() {
            return commonInfo;
        }

        public void setCommonInfo(JSONObject commonInfo) {
            this.commonInfo = commonInfo;
        }
}
